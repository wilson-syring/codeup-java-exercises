public class StringExercise {
    public static void main(String[] args) {

        String noEd = "We don't need no education\nWe don't need no thought control";
        System.out.println(noEd);
        System.out.println();

        String checkIt = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkIt);
        System.out.println();

        String window = "In windows, the main drive is usually C:\\";
        System.out.println(window);
        System.out.println();

        String backSlash = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backSlash);
        System.out.println();
    }
}
